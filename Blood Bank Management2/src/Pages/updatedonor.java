    private void SaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveButtonActionPerformed

        int donorId=Integer.parseInt(jTextField10.getText());
        try 
        {
          Connection con=ConnectionProvider.getCon();
          Statement st=con.createStatement();
          ResultSet rs=st.executeQuery("SELECT * FROM `donor` WHERE `donorId`="+donorId+"");
          
          if(rs.next())
          {
              jTextField1.setText(rs.getString(2));
              jTextField2.setText(rs.getString(3));
              jTextField3.setText(rs.getString(4));
              jTextField4.setText(rs.getString(5));
              jTextField5.setText(rs.getString(6));
              jTextField9.setText(rs.getString(7));
              jTextField6.setText(rs.getString(8));
              jTextField7.setText(rs.getString(9));
              jTextField8.setText(rs.getString(10));
              jTextArea1.setText(rs.getString(11));
              jTextField10.setEditable(false);
          }
          else
          {
              JOptionPane.showMessageDialog(null,"Donor Id does not exitt" );
          }
        } 
        catch (Exception e)
        {
            System.out.println(e);
        }

       
    }//GEN-LAST:event_SaveButtonActionPerformed

