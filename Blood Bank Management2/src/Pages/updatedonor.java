    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        int donorId=Integer.parseInt(jTextField10.getText());
        String name = jTextField1.getText();
        String fathername = jTextField2.getText();
        String mothername = jTextField3.getText();
        String DOB = jTextField4.getText();
        String mobilenumber = jTextField5.getText();
        String gender =jTextField9.getText();
        String email = jTextField6.getText();
        String bloodgroup =jTextField7.getText();
        String city =jTextField8.getText();
        String address = jTextArea1.getText();
       
        try {
            
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            PreparedStatement pstm;
            pstm = con.prepareStatement("UPDATE `donor` SET `name`=?,`fatherName`=?,`motherName`=?,`DOB`=?,`MobileNo`=?,`gender`=?,`email`=?,`bloodGroup`=?,`city`=?,`address`=? WHERE `donorId`="+donorId+"");
            pstm.setString(1, name);
            pstm.setString(2, fathername);
            pstm.setString(3, mothername);
            pstm.setString(4, DOB);
            pstm.setString(5, mobilenumber);
            pstm.setString(6, gender);
            pstm.setString(7, email);
            pstm.setString(8, bloodgroup);
            pstm.setString(9, city);
            pstm.setString(10, address); 
            pstm.executeUpdate();
            JOptionPane.showMessageDialog(null,"Record update succesfully");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
        
       
    }//GEN-LAST:event_jButton1ActionPerformed

